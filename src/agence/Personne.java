package agence;

public class Personne {
	private String nom;
	private String adresse;
	private int numeroTel;
	private String mail;
	
	public Personne(String nom, String adresse, int numeroTel, String mail) {
		this.nom = nom;
		this.adresse = adresse;
		this.numeroTel = numeroTel;
		this.mail = mail;
	}
	
	
}
